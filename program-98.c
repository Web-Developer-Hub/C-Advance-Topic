//bit wise operation in c program....

#include <stdio.h>
int main() {
    char x;
    x = 107;
    x = x << 1;
    printf("%d \n", x);

    x = 107;
    x = x << 2;
    printf("%d \n", x);

    x = 107;
    x = x >> 1;
    printf("%d \n", x);

    x = 107;
    x = x >> 2;
    printf("%d \n", x);

return 0;
}
